monts = ["January", "February", "March", "April"];

print(monts[0], monts[2])

print(monts)

# sort
monts.sort()
print(monts)

# Agregar
monts.append("May")
print(monts)

# Eliminar
del monts[3]
print(monts)

# Eliminar último
monts.pop()
print(monts)

# Eliminar con pop una posición especifica
monts.pop(0);
print(monts)

# Eliminar por nombre

monts.remove("January")
print(monts)





 